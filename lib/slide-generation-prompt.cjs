/**
 * OpenAI スライド生成用プロンプト（営業資料デザインルール）
 * body は「デザインを当てるだけ」の状態＝プレビューにそのまま載る本文のみ（メタラベル禁止）
 */

const TYPE_LABELS = {
  proposal: "提案",
  report: "報告・共有",
  training: "研修・教育",
  sales: "営業・紹介",
  explain: "説明・プレゼン",
  other: "その他",
};

const SYSTEM_PROMPT = `あなたはプロの営業資料デザイナーです。ブリーフをスライドの「構成案」に変換する。配色・装飾の指示は不要。
目的: あとはデザイン（レイアウト・ビジュアル）を当てるだけの粒度。本文はそのままスライド原稿として使えること。
各スライドの title は、その枚の結論が一言で伝わる見出しにする（結論ファースト）。
各スライドの body には、プレビューに表示する本文だけを書く。「結論」「構成」「主メッセージ」「So What」などのメタ見出しやラベル、思考フレームの説明文は一切書かない。
数値（件数・％・倍率・期間など）は本文の箇条書き・表・対比の中に自然に含める（仮値可、未確定は（要確認））。
必ず次のJSON形式のみを返すこと（キーは英語のまま、JSON以外の文字は出さない）:
{"slides":[{"title":"見出し（このスライドの結論が伝わる短文）","body":"### や - や :::compare など、レイアウト記法のみの本文"}]}
slides の配列の長さはユーザー指定の枚数と一致させる。
title は「〇〇の検討」など曖昧な題名を避け、可能なら結論寄りの言い切りにする。`;

function buildUserPrompt(brief) {
  const typeLabel = TYPE_LABELS[brief.type] || TYPE_LABELS.other;

  return [
    "次のブリーフに基づき、各スライドの title と body を日本語で作成してください。",
    "",
    "# 出力の形（重要）",
    "- title: そのスライドの結論が伝わる見出しのみ（メタ情報ではなく、スライドに載る見出し文）。",
    "- body: スライドにそのまま載せる本文だけ。次は禁止:",
    "  - 「結論」「構成」「主メッセージ」「So What」「根拠」などのラベルや枠組みの見出し、角括弧スタイルのメタ見出し",
    "  - デザイナー向けの説明文・指示書・思考プロセスの記述",
    "- body は次のような記法だけで構成する（プレビューでレイアウトされる）:",
    "  - 必ず先頭に `:::subtitle` … `:::` で、そのスライドのサブメッセージを1〜2行（タイトル直下に表示）",
    "  - 続けて `:::cards` / `:::compare` / `:::table` / `:::media` のいずれかで本文を構造化（プレーンな長文のみは禁止）",
    "  - `:::compare` は `### 左` と `### 右` の2列のみ",
    "  - 行頭 `###` のセクション見出し、行頭 `-` の箇条書き",
    "",
    "# 内容のルール（生成時に守るが、ラベルとして出力しない）",
    "- 1スライド = 1メッセージ。テキストは必要最小限。",
    "- 長い説明段落は書かず、箇条書き・対比・カード・表で構造化する。",
    "- 対比（Before/After、従来/新しい、課題/解決など）は可能なら `:::compare` で優先的に表現。",
    "- 「3つの〇〇」などの分解は `:::cards` と ### で。",
    "- 話題が変わる前にはセクション用のスライドを挟む。セクション枚は title に章のメッセージ、body は `### 章タイトル` と必要最小限の行だけ。",
    "- 数値や固有名詞は本文の行の中に直接書く（例: 70%、3ヶ月、10倍）。",
    "",
    "# 禁止",
    "- 抽象的な形容詞の羅列、根拠のないスローガン",
    "- 意味のない箇条書きだけの列挙",
    "",
    "---",
    `- 主目的: ${brief.purpose || "（未入力）"}`,
    `- 対象者: ${brief.audience || "（未入力）"}`,
    `- 種別: ${typeLabel}（${brief.type}）`,
    `- 枚数: ちょうど ${brief.pageCount} 枚（表紙を含む）`,
    "",
    "全体の流れは、状況→論点→対比・分解→推奨→次のアクションなど、枚数に合わせて圧縮。",
    "",
    "レイアウト記法の例:",
    "- 章: `### 01 背景`",
    "- 箇条書き: 行頭に `- `",
    "- 対比:",
    ":::compare",
    "### 左",
    "- 課題の要点",
    "### 右",
    "- 解決の要点",
    ":::",
    "- 3カード: :::cards 内に ### を3つ",
    "- 表: :::table と | 区切り",
    "",
    "JSON 以外の文字は出力しないこと。",
  ].join("\n");
}

module.exports = {
  SYSTEM_PROMPT,
  buildUserPrompt,
};
